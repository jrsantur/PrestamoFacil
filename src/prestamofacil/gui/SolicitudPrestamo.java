    public static void recibirIndex(String valor){
        
        ArrayList<Cliente> cliente = new ArrayList<Cliente>(); 
        ClienteManager clienteManager = new ClienteManager();    
        cliente.add(clienteManager.getClienteById(valor)); 
        jTextField2.setText(
                cliente.get(0).getNombre()+" "+
                cliente.get(0).getApellidos()
        );
        
        jTextField3.setText(cliente.get(0).getDni());
        jTextField4.setText(cliente.get(0).getDireccion());
    }
    
